#  Array - 자료형식
# 리스트 사전 튜플 세트 : 각각의 용도가 조금씩 다르다. 
# 리스트와 튜플은 순서가 있고, 사전식과 세트는 순서가 없다. 
# 리스트는 검색 입력 수정 삭제가 되지만 튜플은 내용 변경이 안된다.

# [] : 리스트를 나타내고, 브라켓 이라고 말함. 순서구분이 있음.
# () : 튜플을 나타내고, 순서구분이 있음.
# {} : 세트를 나타내고, 브레이스 라고 말함. 중복을 허용하지 않으며, 순서구분이 없음. 
# {key:value} : 사전을 나타내고, 매핑을 하는 구조이다. 

